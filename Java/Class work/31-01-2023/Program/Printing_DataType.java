class Printing_DataType
{	
	
	public static void main(String[]args)
	{
		
		System.out.println(10+10); //Integer
		System.out.println(10+10*2/3);
		System.out.println(6*3/8+2-11);
		
		System.out.println("10+10/2");//String
		System.out.println("Hello");
		System.out.println("Done");
		
		System.out.println(false); // Bool
		System.out.println(true);
		
		System.out.println(30.35f);//Float
		System.out.println(4/5.0+2*100);
		
		System.out.println(30.35);// Double
			
	}
}